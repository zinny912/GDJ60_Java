package com.iu.s2.loops;

public class LoopFor_star01 {

	public static void main(String[] args) {
		for(int i=0; i<5; i++) {
			for(int j=0; j<5; j++) {
				System.out.print(i);
			}System.out.println();
		}
	
		for(int a=0; a<5; a++) {
			for(int b=0; b<5; b++) {
				System.out.print("*");
			}System.out.println();
		}

}
}
